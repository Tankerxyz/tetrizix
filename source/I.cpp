{
}

bool I::rotate()
{
	if(getPos() == 0)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│   │   │   │   │			│   │ 0 │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│ 0 │ 1 │ 2 │ 3 │			│   │ 1 │   │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│   │   │   │   │			│   │ 2 │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │ 3 │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[0].append(1,0).subtract(0,1);				
		(*this)[2].append(0,1).subtract(1,0);
		(*this)[3].append(0,2).subtract(2,0);
		setPos(1);
		return true;
	}
	else if(getPos() == 1)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│   │ 0 │   │   │			│   │   │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │ 1 │   │   │			│ 0 │ 1 │ 2 │ 3 │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│   │ 2 │   │   │			│   │   │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │ 3 │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[0].append(0,1).subtract(1,0);
		(*this)[2].append(1,0).subtract(0,1);
		(*this)[3].append(2,0).subtract(0,2);
		setPos(0);
		return true;
	}
	return false;
}
