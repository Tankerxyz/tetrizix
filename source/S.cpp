#include "stdafx.h"
#include "S.h"


bool S::rotate()
{
	if(getPos() == 0)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│   │ 0 │ 1 │   │			│   │ 0 │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│ 2 │ 3 │   │   │			│   │ 3 │ 2 │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │ 1 │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[1].append(0,2);
		(*this)[2].append(2,0);
		setPos(1);
		return true;
	}
	else if(getPos() == 1)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│   │ 0 │   │   │			│   │ 0 │ 1 │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │ 3 │ 2 │   │			│ 2 │ 3 │   │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│   │   │ 1 │   │			│   │   │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[1].subtract(0,2);
		(*this)[2].subtract(2,0);
		setPos(0);
		return true;
	}
	return false;
}

