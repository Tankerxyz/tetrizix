#include "stdafx.h"
#include "Z.h"


bool Z::rotate()
{
	if(getPos() == 0)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│ 0 │ 1 │   │   │			│   │ 1 │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │ 2 │ 3 │   │			│ 3 │ 2 │   │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│   │   │   │   │			│ 0 │   │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[0].append(0,2);
		(*this)[3].subtract(2,0);
		setPos(1);
		return true;
	}
	else if(getPos() == 1)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│   │ 1 │   │   │			│ 0 │ 1 │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│ 3 │ 2 │   │   │			│   │ 2 │ 3 │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│ 0 │   │   │   │			│   │   │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[0].subtract(0,2);
		(*this)[3].append(2,0);
		setPos(0);
		return true;
	}
	return false;
}
