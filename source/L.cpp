#include "stdafx.h"
#include "L.h"



bool L::rotate()
{
	if(getPos() == 0)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│   │   │ 0 │   │			│   │ 1 │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│ 1 │ 2 │ 3 │   │			│   │ 2 │   │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│   │   │   │   │			│   │ 3 │ 0 │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[0].append(0,2);
		(*this)[1].append(1,0).subtract(0,1);
		(*this)[3].append(0,1).subtract(1,0);
		setPos(1);
		return true;
	}
	else if(getPos() == 1)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│   │ 1 │   │   │			│   │   │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │ 2 │   │   │			│ 3 │ 2 │ 1 │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│   │ 3 │ 0 │   │			│ 0 │   │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[0].subtract(2,0);
		(*this)[1].append(1,1);
		(*this)[3].subtract(1,1);
		setPos(2);
		return true;
	}
	else if(getPos() == 2)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│   │   │   │   │			│ 0 │ 3 │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│ 3 │ 2 │ 1 │   │			│   │ 2 │   │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│ 0 │   │   │   │			│   │ 1 │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[0].subtract(0,2);
		(*this)[1].append(0,1).subtract(1,0);
		(*this)[3].append(1,0).subtract(0,1);
		setPos(3);
		return true;
	}
	else if(getPos() == 3)
	{/*				    ┌───┬───┬───┬───┐			┌───┬───┬───┬───┐
						│ 0 │ 3 │   │   │			│   │   │ 0 │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │ 2 │   │   │			│ 1 │ 2 │ 3 │   │
						├───┼───┼───┼───┤	->		├───┼───┼───┼───┤
						│   │ 1 │   │   │			│   │   │   │   │
						├───┼───┼───┼───┤			├───┼───┼───┼───┤
						│   │   │   │   │			│   │   │   │   │
						└───┴───┴───┴───┘			└───┴───┴───┴───┘
																		*/
		(*this)[0].append(2,0);
		(*this)[1].subtract(1,1);
		(*this)[3].append(1,1);
		setPos(0);
		return true;
	}
	return false;
}
